import turtle 
gane=turtle.Turtle()
gane.penup()
gane.goto((-200,40))
gane.pendown()
gane.color("pink")
gane.begin_fill()
gane.pensize(3)
#I symbol
gane.left(90)
gane.forward(180)
gane.right(180)
gane.penup()
gane.forward(180)
gane.left(90)
gane.forward(200)
gane.pendown()
#love symbol
gane.left(50)
gane.forward(133)
gane.circle(50,200)
gane.right(140)
gane.circle(50,200)
gane.forward(133)
gane.left(50)
gane.end_fill()
#U symbol
gane.pencolor("pink")
gane.penup()
gane.forward(180)
gane.left(90)
gane.forward(180)
gane.pendown()
gane.right(180)
gane.forward(150)
gane.circle(50,50)
gane.left(40)
gane.forward(100)
gane.left(40)
gane.circle(50,50)
gane.forward(152)
gane.penup()
gane.left(180)
gane.forward(180)
gane.left(90)
gane.forward(100)
gane.pendown()
turtle.done()